// ── API ──

export function t(key, ...args) {
  const parts = key.split('.');
  let val = T[currentLang];
  for (const p of parts) {
    if (!val || typeof val !== 'object') { val = undefined; break; }
    val = val[p];
  }
  // Fallback to English
  if (val === undefined) {
    val = T.en;
    for (const p of parts) {
      if (!val || typeof val !== 'object') { val = undefined; break; }
      val = val[p];
    }
  }
  if (typeof val !== 'string') return key;
  // Replace {0}, {1}, etc. with args
  return val.replace(/\{(\d+)\}/g, (_, i) => args[i] !== undefined ? args[i] : `{${i}}`);
}

export function getLang() { return currentLang; }

export function setLang(code) {
  if (LANGS.includes(code)) {
    currentLang = code;
    localStorage.setItem('cc-lang', code);
  }
}

export function nextLang() {
  const idx = LANGS.indexOf(currentLang);
  setLang(LANGS[(idx + 1) % LANGS.length]);
  return currentLang;
}

export function getLangs() { return LANGS; }
